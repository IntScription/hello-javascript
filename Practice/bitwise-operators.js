let a = 5; // binary: 101
let b = 2; // binary: 010
console.log(a & b); // binary 000 which is 0
console.log(a | b); // binary 111 which is 7
console.log(a ^ b); // xor operation binary 111 which is 7
console.log(~a); // outputs -6
console.log(a << 1); // outputs 10
console.log(a >> 1); // outputs 2
